angular.module('starter.services', [])

.factory('timelineService', function($http) {
    var baseUrl = 'http://localhost/a/server_side/';
    var baseUrlImage = 'http://localhost/a/server_side/photo_content/';
    return {
        getAll: function() {
            return $http.get(baseUrl+'api.php');
        },
        getId: function (contentId){
            return $http.get(baseUrl+'apiId.php?id_content='+contentId);
        },
        create: function (datateman){
            return $http.post(baseUrl+'insert.php',datateman,{
                headers: {
                    'Content-Type': 'application/x-www-form-urlencoded; charset=UTF-8;'
                }
            });
        },
        update: function (datateman){
            return $http.post(baseUrl+'update.php',datateman,{
                headers: {
                    'Content-Type': 'application/x-www-form-urlencoded; charset=UTF-8;'
                }
            });
        },
        delete: function  (id){
            return $http.get(baseUrl+'delete.php?id='+id);
        }
    };

})

